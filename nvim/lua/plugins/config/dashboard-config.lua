local function week_header()
  local daysoftheweek = {
    'Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday'
  }
  local weekly_tiles = week_ascii_text()
  local day = daysoftheweek[os.date('*t').wday]

  local tbl = {}

  for _ = 1, 7 do 
    table.insert(tbl, '')
  end
  table.insert(tbl,'╭──────────────────────────────────────────────────────────────────────────────╮')
  table.insert(tbl, os.date('│ ⟫⟫⟫                     󰃭 %d-%m-%Y | 󰥔 %H:%M:%S                        ⟪⟪⟪ │'))
  table.insert(tbl,'╰──────────────────────────────────────────────────────────────────────────────╯')

  -- Join Header with Weekly tiles
  for _, value in ipairs(weekly_tiles[day]) do
    table.insert(tbl, value)
  end
  table.insert(tbl, '')

  return tbl
end

local function customHeader()
  local table = {
    table = week_header()
  }
  local ascii_art = table.table
  return ascii_art
end

return  function ()
  vim.cmd([[highlight DashboardHeader guifg=#b5bfe2 gui=nocombine]])

  require('dashboard').setup {
    theme = 'doom',
    disable_move = false,
    hide = {
      statusline = false,
      tabline = true,
    },
    config = {
      header = customHeader(),
      center = {
      {
        icon = "🔍  ",
        desc = "Find File                               ",
        action = function()
          require('telescope.builtin').find_files()
        end,
        key = "f",
      },
      {
        icon = "📂  ",
        desc = "Navigate                               ",
        action = function()
          vim.cmd("Oil")
        end,
        key = "d",
      },
      {
        icon = "⚙️   ",
        desc = "Edit Config                               ",
        action = function()
          require('telescope.builtin').find_files {
            cwd = vim.fn.stdpath("config")
          }
        end,
        key = "e",
      },
      {
        icon = "🖥️  ",
        desc = "Open Terminal                           ",
        action = function()
          vim.cmd.term()
        end,
        key = "t",
      },
      {
        icon = "❌  ",
        desc = "Quit Nvim                               ",
        action = "qa",
        key = "q",
      },
    }
  }
}
